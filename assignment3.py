# Assignment 3 - Type Casting (Data Type Conversion)

# Integer Conversions
a = 10
print("int to float:", float(a))
print("int to complex:", complex(a))
print("int to string:", str(a))
print("int to bool:", bool(a))

# Float Conversions
b = 25.75
print("\nfloat to int:", int(b))
print("float to complex:", complex(b))
print("float to string:", str(b))
print("float to bool:", bool(b))

# String Conversions
c = "100"
print("\nstring to int:", int(c))
print("string to float:", float(c))
print("string to complex:", complex(c))
print("string to bool:", bool(c))

# Boolean Conversions
d = True
print("\nbool to int:", int(d))
print("bool to float:", float(d))
print("bool to complex:", complex(d))
print("bool to string:", str(d))

# Complex Conversions
e = 5 + 3j
print("\ncomplex to string:", str(e))
print("complex to bool:", bool(e))

# List Conversions
l = [1, 2, 3]
print("\nlist to tuple:", tuple(l))
print("list to set:", set(l))
print("list to string:", str(l))

# Tuple Conversions
t = (4, 5, 6)
print("\ntuple to list:", list(t))
print("tuple to set:", set(t))
print("tuple to string:", str(t))

# Set Conversions
s = {7, 8, 9}
print("\nset to list:", list(s))
print("set to tuple:", tuple(s))
print("set to string:", str(s))

# Dictionary Conversions
d1 = {"a": 1, "b": 2}
print("\ndictionary to string:", str(d1))
print("dictionary keys to list:", list(d1))
print("dictionary keys to tuple:", tuple(d1))